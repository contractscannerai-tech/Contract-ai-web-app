type I18nContextType = {
  lang: LangCode;
  setLang: (code: LangCode) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextType>({
  lang: "en",
  setLang: () => {},
  t: (key: string) => key,
});

function getStoredLang(): LangCode {
  try {
    const stored = localStorage.getItem("contractai_lang");
    if (stored && LANGUAGES.some((l) => l.code === stored)) {
      return stored as LangCode;
    }
  } catch {}
  return "en";
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<LangCode>(getStoredLang);

  const setLang = useCallback((code: LangCode) => {
    setLangState(code);
    try { localStorage.setItem("contractai_lang", code); } catch {}
  }, []);

  const t = useCallback((key: string, params?: Record<string, string | number>): string => {
    let value = translations[lang]?.[key] ?? translations.en[key] ?? key;
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        value = value.replace(`{${k}}`, String(v));
      });
    }
    return value;
  }, [lang]);

  return (
    <I18nContext.Provider value={{ lang, setLang, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
